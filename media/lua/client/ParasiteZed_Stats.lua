function ParasiteZed.setStatsToAll()
    local zombies = getCell():getZombieList()
    if not getCell() or not zombies then return end
    for i = 0, zombies:size() - 1 do
        local zed = zombies:get(i)
        if zed and ParasiteZed.isParasiteZed(zed) then
			local num = ParasiteZed.getDayTimeInt()
            ParasiteZed.setTag(zed, num)
            if zed:getModData()['ParasiteZed_Init'] ~= nil then
				ParasiteZed.setStats(zed)
            end
        end
    end
