			sendClientCommand('ParasiteZed', 'CellEvent', { cellName = cellName })
		else
			triggerEvent("OnNestCellChange", x, y, x, y)
		end
	end ]]

	ParasiteZed.doSpawn(sq, false, 'ParasiteZed_Queen')
    ISInventoryPage.renderDirty = true
end

function ParasiteZed.doSpawnQueenNest(sq)
	local midSq = ParasiteZed.getNestCellMidSquare()
	local sprName = tostring( ParasiteZed.MainNest)
	sq = sq or midSq
	local obj = IsoObject.new(getCell(), sq, sprName, false, nil);
	sq:AddTileObject(obj);
	if isClient() then
		obj:transmitCompleteItemToServer();
	end
	triggerEvent("OnObjectAdded", obj)
	ISInventoryPage.renderDirty = true
	return obj
end

function ParasiteZed.getSprName(obj)
    if not obj then return nil end
    local spr = obj:getSprite()
    return spr and spr:getName() or nil
end
-----------------------            ---------------------------

function ParasiteZed.isNest(sprName)
	return sprName and luautils.stringStarts(sprName, "ParasiteZedNest") and ParasiteZed.NestList[sprName] or false
end

function ParasiteZed.isSpawnerNest(sprName)
	return sprName == ParasiteZed.MainNest
end

function ParasiteZed.getNest(source, rad)
	rad = rad or 5
	local cell = getCell()
	source = source or ParasiteZed.getNestCellMidSquare()
	local x, y, z = source:getX(), source:getY(), 0
	for xDelta = -rad, rad do
		for yDelta = -rad, rad do
			local sq = cell:getOrCreateGridSquare(x + xDelta, y + yDelta, z)
			for i=0, sq:getObjects():size()-1 do
				local obj = sq:getObjects():get(i)
				local sprName = ParasiteZed.getSprName(obj)
				if sprName and ParasiteZed.isSpawnerNest(sprName) then
					ParasiteZed.nestSq = sq
					ParasiteZed.nest = obj
					return obj
				end
			end
		end
	end
	return ParasiteZed.nest or nil

end


