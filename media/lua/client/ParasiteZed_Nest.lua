ParasiteZed = ParasiteZed or {}


ParasiteZed.MainNest = "ParasiteZedNest_0"

ParasiteZed.NestList = {
	["ParasiteZedNest_1"] = true,
	["ParasiteZedNest_2"] = true,
	["ParasiteZedNest_3"] = true,
	["ParasiteZedNest_4"] = true,
	["ParasiteZedNest_5"] = true,
}
