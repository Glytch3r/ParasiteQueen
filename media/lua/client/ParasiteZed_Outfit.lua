ParasiteZed = ParasiteZed or {}

function ParasiteZed.isParasiteZed(zed)
    if not (zed or instanceof(zed, "IsoZombie")) then return false end
    return zed:getOutfitName() == "ParasiteZed"
end
function ParasiteZed.isParasiteQueen(zed)
    if not (zed or instanceof(zed, "IsoZombie")) then return false end
    return zed:getOutfitName() == "ParasiteZed_Queen"
end

function ParasiteZed.wearing(pl, fType)
	pl = pl or getPlayer()
	if not instanceof(pl, "IsoPlayer") then return false end
	--if not pl:isAccessLevel('admin') then return false end
	local wornItems = pl:getWornItems()
	if wornItems then
		for i=1, wornItems:size() do
			local item = wornItems:get(i-1):getItem()
			if item:getFullType() == fType  then
				return true
			end
		end
	end
	return false
end


function ParasiteZed.isParasitePl(pl)
	pl = pl or getPlayer()
	return ParasiteZed.wearing(pl, "Base.ParasiteZed")
end

function ParasiteZed.isParasiteQueenPl(pl)
	pl = pl or getPlayer()
	return ParasiteZed.wearing(pl, "Base.ParasiteZed_Queen")
end
function ParasiteZed.isParasiteCharPl(pl)
	pl = pl or getPlayer()
	return ParasiteZed.isParasiteQueenPl(pl) or ParasiteZed.isParasitePl(pl)
end
