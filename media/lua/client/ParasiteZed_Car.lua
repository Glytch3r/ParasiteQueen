ParasiteZed = ParasiteZed or {}



function ParasiteZed.getCar(sq)
	local pl = getPlayer();
	sq = sq or pl:getSquare()
	local car = nil;
	--if not sq then return end
	if pl:getVehicle() then
		car = pl:getVehicle();
	elseif  sq ~= nil then
		car = IsoObjectPicker.Instance:PickVehicle(sq:getX(), sq:getY()) or sq:getVehicleContainer()
	elseif pl:getNearVehicle() then
		car = pl:getNearVehicle();
	elseif pl:getUseableVehicle() then
		car = pl:getUseableVehicle();
	end
	return car
end

function ParasiteZed.carHandler()

	local pl = getPlayer()
	local isEMP = SandboxVars.ParasiteZed_Queen_Queen.EMP or true
	if not isEMP then return end
	local sq = pl:getSquare()
	local x, y = round(pl:getX()),  round(pl:getY())

	local car = ParasiteZed.getCar(sq)
	if car and  ParasiteZed.nest ~= nil then
		local item = car:getPartById("Battery")
		if item then
			local batt = item:getInventoryItem()
			if batt and batt:getUsedDelta() ~= 0 then
				batt:setUsedDelta(0)
			end
		end
		if car:isEngineRunning() then
			ISVehicleMenu.onShutOff(pl)
		end
	end
end
Events.OnEnterVehicle.Add(ParasiteZed.carHandler)
